export const getResourcesByCategory = (category) => {
  return mockResources[category] || [];
};

export const getResourceById = (id) => {
  for (const category in mockResources) {
    const resource = mockResources[category].find(r => r.id === id);
    if (resource) return resource;
  }
  return null;
};

export const getResourcesByServiceType = (serviceType) => {
  const categoryMapping = {
    'weight-management': 'weight',
    'hair-loss': 'hair',
    'ed-treatment': 'ed'
  };
  
  const category = categoryMapping[serviceType] || 'general';
  return mockResources[category] || [];
};
